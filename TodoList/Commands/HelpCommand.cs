
namespace TodoList.Commands;
public class HelpCommand : ICommand
{
    public void Execute()
    {
        Console.WriteLine("""
		profile -o --out - выход из профиля.
		---------------------------------------------------------
		help    - список команд.
		profile - данные пользователя.
		add "текст"   - добавить задачу.
		view - показать задачи.
		status <idx> <status>- присвоить задаче статус.
		delete <idx> - удалить задачу.
		update <idx> new text - обновить текст задачи.
		read <idx> - прочитать полный текст задачи.
		undo - отменить действие.
		redo - вернуть отменённое действие.
		load <кол-во скачиваний> <размер_скачиваний> - загрузить файлы.
		exit    - выход.
		---------------------------------------------------------
		Индексы для команды view:
		--index / -i - индекс задачи.
		--statuses / -s - статусы.
		--update-date / -d - время, дата.
		--all / -a - показать всё.
		---------------------------------------------------------
		Статусы для команды Status:
		NotStarted => не начато.
		InProgress => в процессе.
		Completed => выполнено.
		Postponed => отложено.
		Failed => провалено.
		---------------------------------------------------------
		Команда search:
		--contains <text> - поиск по содержанию текста
		--starts-with <text> - задачи, начинающиеся с текста
		--ends-with <text> - задачи, заканчивающиеся текстом
		--from <date> - задачи с даты (yyyy-MM-dd)
		--to <date> - задачи до даты (yyyy-MM-dd)
		--status <status> - фильтр по статусу
		--sort text/date - сортировка
		--desc - сортировка по убыванию
		--top <n> - показать первые n задач
		""");
    }

	public void Unexecute()
	{

	}
}